#include <iostream>
using namespace std;
int main()
{
    int _1st_number;
    int _2nd_number;
    cout << "enter 1st number: ";
    cin >> _1st_number;
    cout << "enter 2nd number: ";
    cin >> _2nd_number;
    cout << "Addition:  " << _1st_number + _2nd_number << endl;
    cout << "subtraction:  " << _1st_number - _2nd_number << endl;
    cout << "multiplication: " << _1st_number * _2nd_number << endl;
    cout << "division: " << _1st_number / _2nd_number << endl;
    cout << "modulus: " << _1st_number % _2nd_number << endl;
}